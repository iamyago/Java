package ExerciciosGPT;

public class Exercicios1TiposPrimitivos {
    public static void main(String[] args) {
        int valor = 10;
        double valor1 = 1.000;
        float valor3 = 12.5F;
        char valor4 = 'A';
        boolean valor5 = true;
        System.out.println("Valor do int: " + valor);
        System.out.println("Valor do double: " + valor1);
        System.out.println("Valor do float: " + valor3);
        System.out.println("Valor do char: " + valor4);
        System.out.println("Valor do boolean: " + valor5);

    }
}
